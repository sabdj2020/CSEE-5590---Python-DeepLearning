# get numbers as input from user and cast them into int
n1 = int(input("enter number please : "))
n2 = int(input("enter another number please : "))

# arithmetic operation
print("the Power is " + str(n1 ** n2))
print("the sum is: " + str(n1 + n2))
print("the sub is : " + str(n1 - n2))
print("the multiplication  : " + str(n1 * n2))
print("the division: " + str(n1 / n2))
print("Modulus n1 % n2 : " + str(n1 % n2))


